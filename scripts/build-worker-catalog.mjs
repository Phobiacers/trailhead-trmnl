import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const catalogPath = path.join(rootDir, "catalog", "trails.json");
const outputPath = path.join(rootDir, "worker", "catalog.generated.js");

const catalog = JSON.parse(await readFile(catalogPath, "utf8"));
const generated = `// Generated by scripts/build-worker-catalog.mjs. Do not edit by hand.\nexport const CATALOG = ${JSON.stringify(catalog, null, 2)};\n`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, generated);

console.log(`Generated ${path.relative(rootDir, outputPath)} from ${path.relative(rootDir, catalogPath)}`);
